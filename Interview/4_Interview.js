// class component and functional component ? //better functional component
// Functional components are better for modern React development because they
// are simpler, more concise, and more efficient, especially with the introduction
// of React Hooks which provide state management and lifecycle features that were once
// exclusive to class components. While class components were an older, more verbose method,
//  functional components are now the standard and recommended for new projects due to their easier testing,
//  readability, and better alignment with the functional programming paradigm

// ________________________________________________________________________________________________________________________

// Redux used for ?
// Redux is used for managing application state in JavaScript applications, especially
//  complex ones built with frameworks like React, by providing a central, predictable
// store for all global data, making it easier to share data between components, track changes,
//  and improve maintainability.

// ________________________________________________________________________________________________________________________

// What is the use of useEffect in reactjs ?
// The useEffect hook in React is used to perform "side effects" in functional components.
//  Side effects are operations that interact with the outside world or have an impact beyond
// the component's rendering logic. Essentially, useEffect allows you to synchronize your component
//  with external systems.

// ________________________________________________________________________________________________________________________

// What is the use of useRef in reactjs ?
// The useRef hook in React provides a way to create a mutable reference that persists across
// component re-renders without causing the component to re-render when its value changes.
// It returns an object with a single current property, which can hold any mutable JavaScript value.
// ________________________________________________________________________________________________________________________

// What is console.log ?
// console.log() is a built-in JavaScript method used to output messages, variables,
// and other data to the web browser's developer console or a terminal environment.
// It is a fundamental tool for debugging and understanding the execution flow of code.

